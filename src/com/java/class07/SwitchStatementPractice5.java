package com.java.class07;

public class SwitchStatementPractice5 {
	public static void main(String args[]) {
		int input = 3;

		switch (input) {
		default:
			System.out.println("It is not one");
			break;
		case 1:
			System.out.println("First 1");
			break;
		case 2:
			System.out.println("Second 1");
			break;
		}
	}
}
